    struct Queue{
        var items:[move] = []
        
        mutating func enqueue(element: move)
        {
            items.append(element)
        }
        
        mutating func dequeue() -> move?
        {
            if items.isEmpty {
                return nil
            }
            else{
                let tempElement = items.first
                items.remove(at: 0)
                return tempElement
            }
        }
        
        mutating func notEmpty() -> Bool
        {
            if items.count == 0 {
                return false
            }
            else {
                return true
            }
        }
    }
    
