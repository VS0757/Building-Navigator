    func bfs(startx: Int, starty: Int, endx: Int, endy: Int) -> Array<String> {
        var Q = Queue()
        var visited = Array(repeating: Array(repeating: false, count: 10), count: 5)
        let xpos = [-1, 1, 0, 0]
        let ypos = [0, 0, 1, -1]
        let word = ["up", "down", "right", "left"]
        var x = startx
        var y = starty
        visited[x][y] = true
        Q.enqueue(element: move(x: startx, y: starty, steps: []))
        
        while(Q.notEmpty()) {
            let temp = Q.dequeue()!
            var tempSteps = temp.steps
            x = temp.x
            y = temp.y
            
            if x == endx && y == endy {
                return tempSteps
            }
            
            for i in 0...3 {
                if x + xpos[i] < 5 && x + xpos[i] > -1 && y + ypos[i] < 10 && y + ypos[i] > -1 && mapArray[x + xpos[i]][y + ypos[i]] == 0 && !visited[x + xpos[i]][y + ypos[i]] {
                    visited[x + xpos[i]][y + ypos[i]] = true
                    tempSteps.append(word[i])
                    Q.enqueue(element: move(x: x + xpos[i], y: y + ypos[i], steps: tempSteps))
                    tempSteps.remove(at: tempSteps.count - 1)
                }
            }
        }
        return ["Not Possible"]
    }
